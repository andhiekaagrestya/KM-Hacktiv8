// operator (>, <, ==) -> mengembalikan nilai true / false (boolean)
console.log(10 > 7) // true
console.log(7 > 10) // false
console.log(7 == 7) // true
console.log(7 == 10) // false

console.log(5 == "5") // true
console.log(5 === "5") // false (karena dibandingkan sampai ke tipe datanya)
console.log(5 === 5) // true
console.log("5" === 5) // true